N = int(input())
events = []
for _ in range(N):
  s, e = map(int,input().split())
  events.append((s, 1))
  events.append((e, -1))

events.sort()
ans = 0
crr = 0
for _, d in events:
  crr += d
  ans = max(ans, crr)

print(ans)
